class LLMRouter:
    """
    LLM-based intent router

    WHY: Regex cannot understand context or nuance.
    LLM can detect technical intent even in casual language.

    Example:
    - "เห้ย ช่วยออกแบบไฟห้องครัวหน่อย" → MOZART (technical)
    - "คุณคิดยังไงกับจริยธรรม AI" → AMADEUS (philosophical)
    """

    ROUTER_PROMPT = """You are an intent classifier for an engineering AI system.

The system has TWO modes:
1. MOZART (RAG/Engineering): Technical questions about electrical design, calculations, standards, AutoCAD
2. AMADEUS (AGI/Chat): General conversation, philosophy, ethics, non-technical questions

Examples:
User: "ออกแบบไฟห้องครัวให้หน่อย" → MOZART
User: "คำนวณ voltage drop ให้ที" → MOZART
User: "คุณคิดยังไงกับความหมายของชีวิต" → AMADEUS
User: "เล่าเรื่องตลกหน่อย" → AMADEUS

User input: "{user_input}"

Respond ONLY with: MOZART or AMADEUS
"""

    def __init__(self):
        self.client = httpx.AsyncClient(timeout=10.0)

    async def route_with_llm(
        self,
        user_input: str,
        dialogue_history: Optional[List[Dict]] = None
    ) -> RoutingDecision:
        """
        Route using LLM instead of regex

        WHY THIS IS BETTER:
        - Understands context from dialogue history
        - Can handle misspellings, slang, mixed languages
        - Higher accuracy than keyword matching
        """
        # Build prompt with context
        prompt = self.ROUTER_PROMPT.format(user_input=user_input)

        # Add dialogue history if available
        if dialogue_history:
            context = "\n".join([
                f"{msg['role']}: {msg['content']}"
                for msg in dialogue_history[-3:]  # Last 3 turns
            ])
            prompt = f"Previous context:\n{context}\n\n{prompt}"

        # Call LLM (Ollama API)
        try:
            response = await self.client.post(
                LLM_ROUTER_ENDPOINT,
                json={
                    "model": LLM_ROUTER_MODEL,
                    "prompt": prompt,
                    "stream": False
                }
            )
            response.raise_for_status()

            result = response.json()
            answer = result.get("response", "").strip().upper()

            if "MOZART" in answer:
                return RoutingDecision(
                    mode=IntentMode.COPILOT,
                    confidence=0.90,
                    reasoning="LLM detected technical/engineering intent",
                    keywords=["llm_classified"]
                )
            else:
                return RoutingDecision(
                    mode=IntentMode.AGI,
                    confidence=0.90,
                    reasoning="LLM detected conversational intent",
                    keywords=["llm_classified"]
                )

        except Exception as e:
            print(f"[GATEWAY] LLM router failed: {e}, falling back to regex")
            # Fallback to old regex method
            return self._fallback_regex_route(user_input)

