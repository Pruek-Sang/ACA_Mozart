@app.post("/orchestrate", response_model=GatewayResponse)
@trace_gateway(name="gateway_orchestrate")
async def orchestrate(
    request: GatewayRequest,
    x_trace_id: Optional[str] = Header(None)
) -> GatewayResponse:
    """
    Enhanced orchestrator with dialogue support

    New Flow:
    1. Check if this is part of ongoing dialogue
    2. If yes, route to Dialogue Manager first
    3. If dialogue complete, route to Mozart → MCP
    4. If not dialogue, use LLM router
    """
    start_time = time.time()
    trace_id = x_trace_id or f"gateway-{int(time.time() * 1000)}"

    # Check for ongoing dialogue session
    if request.session_id:
        # Forward to Dialogue Manager
        dialogue_response = await _handle_dialogue_turn(request, trace_id)

        if dialogue_response["ready_for_mcp"]:
            # Dialogue complete → Execute MCP
            mcp_result = await proxy.call_copilot(request, trace_id)

            return GatewayResponse(
                mode=IntentMode.COPILOT,
                data={
                    "dialogue": dialogue_response,
                    "mcp_result": mcp_result
                },
                processing_time_ms=int((time.time() - start_time) * 1000),
                trace_id=trace_id,
                routing_decision=RoutingDecision(
                    mode=IntentMode.COPILOT,
                    confidence=1.0,
                    reasoning="Dialogue completed, MCP executed",
                    keywords=["dialogue_complete"]
                )
            )
        else:
            # Continue dialogue
            return GatewayResponse(
                mode=IntentMode.COPILOT,
                data=dialogue_response,
                processing_time_ms=int((time.time() - start_time) * 1000),
                trace_id=trace_id,
                routing_decision=RoutingDecision(
                    mode=IntentMode.COPILOT,
                    confidence=1.0,
                    reasoning="Dialogue in progress",
                    keywords=["dialogue_turn"]
                )
            )

    # No dialogue → Use LLM router
    router = LLMRouter()
    decision = await router.route_with_llm(request.input)

    # Delegate to appropriate zone
    if decision.mode == IntentMode.COPILOT:
        data = await proxy.call_copilot(request, trace_id)
    else:
        data = await proxy.call_agi(request, trace_id)

    return GatewayResponse(
        mode=decision.mode,
        data=data,
        processing_time_ms=int((time.time() - start_time) * 1000),
        trace_id=trace_id,
        routing_decision=decision
    )


async def _handle_dialogue_turn(request: GatewayRequest, trace_id: str) -> Dict:
    """Helper to call Dialogue Manager"""
    async with httpx.AsyncClient() as client:
        response = await client.post(
            f"{DIALOGUE_MANAGER_ENDPOINT}/dialogue/turn",
            json={
                "session_id": request.session_id,
                "user_id": request.user_id,
                "user_input": request.input
            },
            headers={"X-Trace-ID": trace_id}
        )
        response.raise_for_status()
        return response.json()