# -*- coding: utf-8 -*-
"""Layout Optimizer Module"""

import math

class LayoutOptimizer:
    """คำนวณระยะทางที่สั้นที่สุด"""

    @staticmethod
    def optimize_outlet_placement(room_width: float, room_length: float,
                                spacing: float = 1200) -> List[Dict[str, float]]:
        """หาตำแหน่งที่เหมาะสมสำหรับติดตั้ง outlet"""

        placements = []

        # Along walls
        # Top wall
        x = spacing
        while x < room_width - spacing:
            placements.append({"x": x, "y": 100})
            x += spacing

        # Bottom wall
        x = spacing
        while x < room_width - spacing:
            placements.append({"x": x, "y": room_length - 100})
            x += spacing

        # Left wall
        y = spacing
        while y < room_length - spacing:
            placements.append({"x": 100, "y": y})
            y += spacing

        # Right wall
        y = spacing
        while y < room_length - spacing:
            placements.append({"x": room_width - 100, "y": y})
            y += spacing

        return placements

