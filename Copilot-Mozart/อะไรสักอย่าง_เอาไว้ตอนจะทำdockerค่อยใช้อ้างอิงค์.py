#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""Conduit Sizer Module"""

class ConduitSizer:
    """เลือกขนาดท่อ"""

    # Conduit sizes and fill capacity
    CONDUIT_SIZES = {
        16: {"area": 201, "max_fill": 0.4},  # mm²
        20: {"area": 314, "max_fill": 0.4},
        25: {"area": 491, "max_fill": 0.4},
        32: {"area": 804, "max_fill": 0.4}
    }

    # Wire areas
    WIRE_AREAS = {
        1.5: 7.1,
        2.5: 11.9,
        4.0: 19.0,
        6.0: 28.8
    }

    @classmethod
    def select_conduit_size(cls, wire_sizes: List[float],
                           wire_count: List[int]) -> Dict[str, Any]:
        """เลือกขนาดท่อตามจำนวนและขนาดสาย"""

        total_wire_area = 0
        for size, count in zip(wire_sizes, wire_count):
            wire_area = cls.WIRE_AREAS.get(size, 0)
            total_wire_area += wire_area * count

        # Select conduit size
        for size, data in sorted(cls.CONDUIT_SIZES.items()):
            max_fill_area = data["area"] * data["max_fill"]
            if total_wire_area <= max_fill_area:
                return {
                    "size_mm": size,
                    "area_mm2": data["area"],
                    "fill_pct": (total_wire_area / data["area"]) * 100,
                    "wire_area_total": total_wire_area
                }

        # Return largest if none fits
        largest = max(cls.CONDUIT_SIZES.keys())
        largest_data = cls.CONDUIT_SIZES[largest]

        return {
            "size_mm": largest,
            "area_mm2": largest_data["area"],
            "fill_pct": (total_wire_area / largest_data["area"]) * 100,
            "wire_area_total": total_wire_area,
            "warning": "Conduit may be overfilled"
        }

