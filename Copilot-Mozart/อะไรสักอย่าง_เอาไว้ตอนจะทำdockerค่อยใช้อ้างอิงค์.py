#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""Compliance Checker Module"""

class ComplianceChecker:
    """ตรวจสอบมาตรฐาน"""

    @staticmethod
    def check_mea_compliance(wire_size: float, current: float,
                           room_type: str) -> List[Dict[str, Any]]:
        """ตรวจสอบตามมาตรฐาน MEA"""
        violations = []

        # Minimum wire size for kitchen
        if room_type == "kitchen" and wire_size < 2.5:
            violations.append({
                "code": "MEA_MIN_WIRE_KITCHEN",
                "severity": "ERROR",
                "message": "ห้องครัวต้องใช้สายขนาดอย่างน้อย 2.5 มม."
            })

        # Maximum outlets per circuit
        if current > 16:
            violations.append({
                "code": "MEA_MAX_OUTLETS",
                "severity": "WARNING",
                "message": "กระแสเกิน 16A ควรแยกวงจร"
            })

        return violations

